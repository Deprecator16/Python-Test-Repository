# This program will get your birth month

month = input("Type your birthmonth here: ")

if (month == "January"):
    print("J")
elif (month == "February"):
    print("F")
elif (month == "March"):
    print("M")
elif (month == "April"):
    print("A")
elif (month == "May"):
    print("Ma")
elif (month == "June"):
    print("J")
elif (month == "July"):
    print("Ju")
elif (month == "August"):
    print("Au")
else:
    print("You didn't enter a month correctly.")
